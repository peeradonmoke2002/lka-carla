#!/usr/bin/python3
"""
LKA Dataset Collection Node

2-class YOLO segmentation labels:
  class 0 (left_marking)  — RoadLine (class 6): center dashed/solid line
  class 1 (right_edge)    — Sidewalk (class 8) inside polygon ROI

ROI polygon is loaded from roi.yaml (generated by set_roi.py).
Both classes are detected only inside the polygon ROI to avoid false positives.

Topics consumed:
  /carla/ego_vehicle/CAM_FRONT/image
  /carla/ego_vehicle/semantic_segmentation_front/image

Output:
  <output_dir>/images/train|val/<frame>.jpg
  <output_dir>/labels/train|val/<frame>.txt
  <output_dir>/data.yaml
"""

import os
import random
import yaml

import cv2
import numpy as np
import rclpy
import rclpy.parameter
from rclpy.node import Node
from sensor_msgs.msg import Image
from cv_bridge import CvBridge
import message_filters

try:
    import carla
    CARLA_AVAILABLE = True
except ImportError:
    CARLA_AVAILABLE = False

# ── CARLA semantic colors (BGR) ────────────────────────────────────────────────
ROADLINE_BGR     = np.array([ 50, 234, 157], dtype=np.uint8)
SIDEWALK_BGR     = np.array([232,  35, 244], dtype=np.uint8)

# ── Detection parameters ───────────────────────────────────────────────────────
MIN_CONTOUR_AREA   = 30
MIN_SIDEWALK_AREA  = 100
CONTOUR_APPROX_EPS = 0.002
JUNCTION_LOOKAHEAD = 15
JUNCTION_SKIP      = 30
VAL_FRACTION       = 0.15
JPEG_QUALITY       = 95

# ── ROI yaml path ──────────────────────────────────────────────────────────────
ROI_YAML = os.path.join(os.path.dirname(os.path.realpath(__file__)), 'roi.yaml')


def load_roi_polygon(path: str):
    """Load polygon points from roi.yaml. Returns np.array shape (N,2) or None."""
    if not os.path.exists(path):
        return None
    with open(path) as f:
        data = yaml.full_load(f)
    raw = data.get('roi', {}).get('polygon', None)
    if raw is None:
        return None
    return np.array([list(p) for p in raw], dtype=np.int32)


class DatasetCollectorNode(Node):

    def __init__(self):
        super().__init__('dataset_collector')
        self.set_parameters([rclpy.parameter.Parameter(
            'use_sim_time', rclpy.Parameter.Type.BOOL, True)])

        self.declare_parameter('output_dir',    os.path.expanduser('~/lka_dataset'))
        self.declare_parameter('max_images',    2000)
        self.declare_parameter('skip_junctions', True)
        self.declare_parameter('carla_host',    'localhost')
        self.declare_parameter('carla_port',    2000)
        self.declare_parameter('image_topic',
                               '/carla/ego_vehicle/CAM_FRONT/image')
        self.declare_parameter('semantic_topic',
                               '/carla/ego_vehicle/semantic_segmentation_front/image')
        self.declare_parameter('sync_slop',     0.05)

        self.output_dir     = self.get_parameter('output_dir').value
        self.max_images     = self.get_parameter('max_images').value
        self.skip_junctions = self.get_parameter('skip_junctions').value
        carla_host          = self.get_parameter('carla_host').value
        carla_port          = self.get_parameter('carla_port').value
        image_topic         = self.get_parameter('image_topic').value
        semantic_topic      = self.get_parameter('semantic_topic').value
        sync_slop           = self.get_parameter('sync_slop').value

        for split in ('train', 'val'):
            os.makedirs(os.path.join(self.output_dir, 'images', split), exist_ok=True)
            os.makedirs(os.path.join(self.output_dir, 'labels', split), exist_ok=True)

        # ── Load ROI polygon ───────────────────────────────────────────────────
        self.roi_polygon = load_roi_polygon(ROI_YAML)
        if self.roi_polygon is not None:
            self.get_logger().info(
                f'ROI polygon loaded: {self.roi_polygon.tolist()}')
        else:
            self.get_logger().warn(
                'roi.yaml not found — no ROI applied. '
                'Run set_roi.py to define a polygon ROI.')

        # ── CARLA client (junction detection) ─────────────────────────────────
        self.carla_world = None
        self.ego_vehicle = None

        if self.skip_junctions and CARLA_AVAILABLE:
            try:
                client = carla.Client(carla_host, carla_port)
                client.set_timeout(10.0)
                self.carla_world = client.get_world()
                self.get_logger().info(f'CARLA connected at {carla_host}:{carla_port}')
            except Exception as e:
                self.get_logger().warn(
                    f'Cannot connect to CARLA ({e}). Junction detection disabled.')
                self.skip_junctions = False
        elif self.skip_junctions and not CARLA_AVAILABLE:
            self.get_logger().warn('carla package not found. Junction detection disabled.')
            self.skip_junctions = False

        self.bridge               = CvBridge()
        self.saved_count          = 0
        self.frame_count          = 0
        self.junction_skip_remain = 0
        self.poly_mask            = None   # built on first frame

        rgb_sub = message_filters.Subscriber(self, Image, image_topic)
        sem_sub = message_filters.Subscriber(self, Image, semantic_topic)
        self.sync = message_filters.ApproximateTimeSynchronizer(
            [rgb_sub, sem_sub], queue_size=20, slop=sync_slop)
        self.sync.registerCallback(self._callback)

        self.get_logger().info(
            f'Dataset collector ready — {self.output_dir} '
            f'(max {self.max_images}) | classes: left_marking + right_edge')

    # ── Helpers ────────────────────────────────────────────────────────────────

    def _build_poly_mask(self, h, w):
        """Build polygon mask at image resolution."""
        mask = np.zeros((h, w), dtype=np.uint8)
        if self.roi_polygon is not None:
            cv2.fillPoly(mask, [self.roi_polygon], 255)
        else:
            mask[:] = 255   # full image if no ROI
        return mask

    def _contours_to_labels(self, mask, class_id, w, h, min_area):
        """Find contours in mask and return YOLO label strings."""
        lines = []
        cnts, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        for c in cnts:
            if cv2.contourArea(c) < min_area:
                continue
            eps  = CONTOUR_APPROX_EPS * cv2.arcLength(c, True)
            poly = cv2.approxPolyDP(c, eps, True)
            if len(poly) < 3:
                continue
            pts = poly.reshape(-1, 2).astype(np.float32)
            pts[:, 0] /= w
            pts[:, 1] /= h
            coords = ' '.join(f'{x:.6f} {y:.6f}' for x, y in pts)
            lines.append(f'{class_id} {coords}')
        return lines

    # ── Main callback ──────────────────────────────────────────────────────────

    def _callback(self, rgb_msg: Image, sem_msg: Image):
        self.frame_count += 1

        if self.saved_count >= self.max_images:
            return

        if self.frame_count % 30 == 1:
            self.get_logger().info(
                f'[DBG] frame={self.frame_count} saved={self.saved_count}')

        if self.junction_skip_remain > 0:
            self.junction_skip_remain -= 1
            return

        if self.skip_junctions and self._near_junction():
            self.junction_skip_remain = JUNCTION_SKIP
            return

        rgb = self.bridge.imgmsg_to_cv2(rgb_msg, desired_encoding='bgr8')
        sem = self.bridge.imgmsg_to_cv2(sem_msg, desired_encoding='bgr8')
        h, w = rgb.shape[:2]

        # Build poly_mask once at actual image resolution
        if self.poly_mask is None:
            self.poly_mask = self._build_poly_mask(h, w)
            self.get_logger().info(f'[DBG] image size: {w}x{h} — poly mask built')

        label_lines = []

        # ── Class 0: left_marking — RoadLine inside ROI ────────────────────────
        rl_full = cv2.inRange(sem, ROADLINE_BGR, ROADLINE_BGR)
        rl_roi  = cv2.bitwise_and(rl_full, self.poly_mask)
        label_lines += self._contours_to_labels(rl_roi, 0, w, h, MIN_CONTOUR_AREA)

        # ── Class 1: right_edge — sidewalk pixels inside crop polygon ────────
        # The crop polygon is drawn to cover only our lane (right side of road).
        # So sidewalk pixels inside the crop = right lane boundary only.
        # Keep largest contour to filter any residual noise.
        sw_full = cv2.inRange(sem, SIDEWALK_BGR, SIDEWALK_BGR)
        sw_roi  = cv2.bitwise_and(sw_full, self.poly_mask)
        sw_cnts, _ = cv2.findContours(
            sw_roi, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        valid_sw = [c for c in sw_cnts if cv2.contourArea(c) >= MIN_SIDEWALK_AREA]
        if valid_sw:
            largest = max(valid_sw, key=cv2.contourArea)
            eps  = CONTOUR_APPROX_EPS * cv2.arcLength(largest, True)
            poly = cv2.approxPolyDP(largest, eps, True)
            if len(poly) >= 3:
                # Clip polygon points to stay inside ROI mask
                clip = np.zeros((h, w), dtype=np.uint8)
                cv2.fillPoly(clip, [poly.reshape(-1,1,2)], 255)
                clip = cv2.bitwise_and(clip, self.poly_mask)
                clip_cnts, _ = cv2.findContours(
                    clip, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
                if clip_cnts:
                    cc = max(clip_cnts, key=cv2.contourArea)
                    ep2  = CONTOUR_APPROX_EPS * cv2.arcLength(cc, True)
                    poly2 = cv2.approxPolyDP(cc, ep2, True)
                    if len(poly2) >= 3:
                        pts = poly2.reshape(-1, 2).astype(np.float32)
                        pts[:, 0] /= w
                        pts[:, 1] /= h
                        coords = ' '.join(f'{x:.6f} {y:.6f}' for x, y in pts)
                        label_lines.append(f'1 {coords}')

        if not label_lines:
            if self.frame_count % 30 == 1:
                self.get_logger().info('[DBG] no labels this frame — skipping')
            return

        # ── Save ───────────────────────────────────────────────────────────────
        split = 'val' if random.random() < VAL_FRACTION else 'train'
        name  = f'{self.saved_count:06d}'

        cv2.imwrite(
            os.path.join(self.output_dir, 'images', split, f'{name}.jpg'),
            rgb,
            [cv2.IMWRITE_JPEG_QUALITY, JPEG_QUALITY])

        with open(os.path.join(self.output_dir, 'labels', split, f'{name}.txt'), 'w') as f:
            f.write('\n'.join(label_lines))

        self.saved_count += 1

        if self.saved_count % 10 == 0:
            self.get_logger().info(
                f'Progress: {self.saved_count}/{self.max_images} '
                f'(frame {self.frame_count})')

        if self.saved_count >= self.max_images:
            self._write_data_yaml()
            self.get_logger().info(
                f'Collection complete — {self.saved_count} images at {self.output_dir}')

    # ── Junction detection ─────────────────────────────────────────────────────

    def _near_junction(self) -> bool:
        if self.ego_vehicle is None:
            self.ego_vehicle = self._find_ego_vehicle()
        if self.ego_vehicle is None:
            return False
        try:
            carla_map = self.carla_world.get_map()
            loc       = self.ego_vehicle.get_transform().location
            wp        = carla_map.get_waypoint(loc, project_to_road=True)
            for _ in range(JUNCTION_LOOKAHEAD):
                nexts = wp.next(1.0)
                if not nexts:
                    break
                wp = nexts[0]
                if wp.is_junction or len(wp.next(1.0)) > 1:
                    return True
        except Exception as e:
            self.get_logger().debug(f'Junction check error: {e}')
        return False

    def _find_ego_vehicle(self):
        try:
            for actor in self.carla_world.get_actors():
                if actor.attributes.get('role_name') == 'ego_vehicle':
                    self.get_logger().info(f'Ego vehicle found: {actor.type_id}')
                    return actor
        except Exception as e:
            self.get_logger().debug(f'Actor search error: {e}')
        return None

    # ── data.yaml ─────────────────────────────────────────────────────────────

    def _write_data_yaml(self):
        data = {
            'path':  self.output_dir,
            'train': 'images/train',
            'val':   'images/val',
            'nc':    2,
            'names': ['left_marking', 'right_edge'],
        }
        path = os.path.join(self.output_dir, 'data.yaml')
        with open(path, 'w') as f:
            yaml.dump(data, f, default_flow_style=False, sort_keys=False)
        self.get_logger().info(f'data.yaml written → {path}')


# ── Entry point ────────────────────────────────────────────────────────────────

def main(args=None):
    rclpy.init(args=args)
    node = DatasetCollectorNode()
    try:
        rclpy.spin(node)
    except KeyboardInterrupt:
        pass
    finally:
        if node.saved_count > 0 and node.saved_count < node.max_images:
            node._write_data_yaml()
            node.get_logger().info(
                f'Interrupted — {node.saved_count} images saved')
        node.destroy_node()
        rclpy.shutdown()


if __name__ == '__main__':
    main()
